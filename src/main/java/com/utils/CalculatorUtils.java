package com.utils;

public class CalculatorUtils {
    public static int summarize(int s1, int s2){
        return s1 + s2;
    }

    public static int substraction(int d1, int d2){
        return d1 - d2;
    }

    public static int multiplication(int m1, int m2){
        return m1 * m2;
    }

    public static int dividing(int d1, int d2){
        return d1 / d2;
    }
}
